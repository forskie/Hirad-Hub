// ── Theme ──
const html = document.documentElement;
const btn = document.getElementById('theme-toggle');
const sun = document.getElementById('icon-sun');
const moon = document.getElementById('icon-moon');

function applyTheme(t) {
    if (t === 'dark') {
        html.classList.add('dark');
        sun.style.display = 'block';
        moon.style.display = 'none';
    } else {
        html.classList.remove('dark');
        sun.style.display = 'none';
        moon.style.display = 'block';
    }
}
const saved = localStorage.getItem('hh-theme') ||
    (window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light');
applyTheme(saved);
btn.addEventListener('click', () => {
    const next = html.classList.contains('dark') ? 'light' : 'dark';
    localStorage.setItem('hh-theme', next);
    applyTheme(next);
});

// ── Mobile menu ──
document.getElementById('hamburger').addEventListener('click', () => {
    document.getElementById('mobile-menu').classList.toggle('open');
});

// ── Active nav link ──
document.querySelectorAll('.nav-link').forEach(link => {
    if (window.location.href.startsWith(link.href)) link.classList.add('active');
});

// ── Dropdown — JS controlled (надёжнее hover) ──
const avatarBtn = document.getElementById('avatar-btn');
const dropdownMenu = document.getElementById('dropdown-menu');

if (avatarBtn && dropdownMenu) {
    let closeTimer = null;

    function openDropdown() {
        clearTimeout(closeTimer);
        dropdownMenu.classList.add('open');
    }
    function scheduleClose() {
        closeTimer = setTimeout(() => dropdownMenu.classList.remove('open'), 150);
    }

    // Открывается при наведении на аватарку или меню
    avatarBtn.addEventListener('mouseenter', openDropdown);
    avatarBtn.addEventListener('mouseleave', scheduleClose);
    dropdownMenu.addEventListener('mouseenter', openDropdown);
    dropdownMenu.addEventListener('mouseleave', scheduleClose);

    // Также открывается по клику (для тач устройств)
    avatarBtn.addEventListener('click', () => {
        dropdownMenu.classList.toggle('open');
    });

    // Закрывается при клике вне
    document.addEventListener('click', (e) => {
        if (!avatarBtn.contains(e.target) && !dropdownMenu.contains(e.target)) {
            dropdownMenu.classList.remove('open');
        }
    });
}

// ── Live search ──
const searchInput = document.getElementById('search-input');
const searchResults = document.getElementById('search-results');
let searchTimer = null;

if (searchInput && searchResults) {

    searchInput.addEventListener('input', () => {
        clearTimeout(searchTimer);
        const q = searchInput.value.trim();

        if (!q) {
            searchResults.classList.remove('open');
            searchResults.innerHTML = '';
            return;
        }

        searchTimer = setTimeout(async () => {
            try {
                const res = await fetch(`/search/?q=${encodeURIComponent(q)}&format=json`);
                const data = await res.json();
                renderSearchResults(data, q);
            } catch {
                searchResults.classList.remove('open');
            }
        }, 300);
    });

    function renderSearchResults(data, q) {
        const { users = [], posts = [], notes = [], books = [] } = data;
        const total = users.length + posts.length + notes.length + books.length;

        if (total === 0) {
            const safeQ = String(q).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/"/g, '&quot;');
            searchResults.innerHTML = `<div class="search-empty">${I18N.t('search.empty', { q: safeQ })}</div>`;
            searchResults.classList.add('open');
            return;
        }

        let html = '';

        if (users.length) {
            html += `<div class="search-result-section">${I18N.t('search.users')}</div>`;
            users.forEach(u => {
                html += `<a href="/user/profile/${u.username}/" class="search-result-item">
                <div style="width:28px;height:28px;border-radius:50%;background:#1e1d3a;border:1px solid var(--accent);display:flex;align-items:center;justify-content:center;font-size:11px;color:#a5b4fc;flex-shrink:0;">
                  ${u.username[0].toUpperCase()}
                </div>
                <div>
                  <p style="font-size:13px;font-weight:500;color:var(--text);">${u.full_name || u.username}</p>
                  <p style="font-size:11px;color:var(--muted);">@${u.username} · Lv.${u.level}</p>
                </div>
              </a>`;
            });
        }

        if (posts.length) {
            html += `<div class="search-result-section">${I18N.t('search.posts')}</div>`;
            posts.forEach(p => {
                html += `
              <a href="/post/${p.id}/" class="search-result-item">
                <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="var(--muted)" stroke-width="2" style="flex-shrink:0;"><path d="M21 15a2 2 0 0 1-2 2H7l-4 4V5a2 2 0 0 1 2-2h14a2 2 0 0 1 2 2z"/></svg>
                <p style="font-size:13px;color:var(--text);overflow:hidden;text-overflow:ellipsis;white-space:nowrap;">${p.text}</p>
              </a>`;
            });
        }

        if (notes.length) {
            html += `<div class="search-result-section">${I18N.t('search.notes')}</div>`;
            notes.forEach(n => {
                html += `
              <a href="/note/${n.id}" class="search-result-item">
                <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="var(--muted)" stroke-width="2" style="flex-shrink:0;"><path d="M14 2H6a2 2 0 0 0-2 2v16a2 2 0 0 0 2 2h12a2 2 0 0 0 2-2V8z"/><polyline points="14 2 14 8 20 8"/></svg>
                <p style="font-size:13px;color:var(--text);">${n.title}</p>
              </a>`;
            });
        }

        if (books.length) {
            html += `<div class="search-result-section">${I18N.t('search.books')}</div>`;
            books.forEach(b => {
                html += `
                <a href="/library/books/${b.id}/" class="search-result-item">
                <svg width="14" height="14" viewBox="0 0 24 24" fill="none" stroke="var(--muted)" stroke-width="2" style="flex-shrink:0;"><path d="M4 19.5A2.5 2.5 0 0 1 6.5 17H20"/><path d="M6.5 2H20v20H6.5A2.5 2.5 0 0 1 4 19.5v-15A2.5 2.5 0 0 1 6.5 2z"/></svg>
                <p style="font-size:13px;color:var(--text);">${b.title}</p>
                </a>`;
            });
        }

        searchResults.innerHTML = html;
        searchResults.classList.add('open');
    }

    // Закрыть при клике вне поиска
    document.addEventListener('click', (e) => {
        if (!searchInput.contains(e.target) && !searchResults.contains(e.target)) {
            searchResults.classList.remove('open');
        }
    });

    // Enter — переход на страницу поиска
    searchInput.addEventListener('keydown', (e) => {
        if (e.key === 'Enter') {
            e.preventDefault();
            window.location.href = `/search/?q=${encodeURIComponent(searchInput.value)}`;
        }
    });
}


