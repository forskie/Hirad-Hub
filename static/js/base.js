const I18N = {


    lang: localStorage.getItem('hh-lang') || 'ru',

    t(key, params) {
        const lang = this.lang;
        let s = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key])
            || (TRANSLATIONS['en'] && TRANSLATIONS['en'][key])
            || key;
        if (params && typeof s === 'string') {
            Object.keys(params).forEach((k) => {
                s = s.split(`{${k}}`).join(params[k]);
            });
        }
        return s;
    },

    apply() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const attr = el.getAttribute('data-i18n-attr');
            const val = this.t(key);
            if (attr) {
                el.setAttribute(attr, val);
            } else {
                el.textContent = val;
            }
        });

        document.documentElement.lang = this.lang;

        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active', btn.dataset.lang === this.lang);
        });
    },

    set(lang) {
        if (!TRANSLATIONS[lang]) return;
        this.lang = lang;
        localStorage.setItem('hh-lang', lang);
        this.apply();
        window.dispatchEvent(new CustomEvent('hh-lang'));
    },


    init() {
        this.apply();
        if (typeof document.body !== 'undefined') {
            document.body.addEventListener('htmx:afterSwap', () => this.apply());
            document.body.addEventListener('htmx:afterSettle', () => this.apply());
        }
    }
};

document.addEventListener('DOMContentLoaded', () => I18N.init());